<script>
// Counter animation
document.addEventListener('DOMContentLoaded', function () {
    const counters = document.querySelectorAll('.stat-number[data-target]');
    const speed = 60;

    const animateCounter = (el) => {
        const target = +el.getAttribute('data-target');
        const step = Math.ceil(target / speed);
        let current = 0;
        const timer = setInterval(() => {
            current += step;
            if (current >= target) {
                el.textContent = target.toLocaleString() + '+';
                clearInterval(timer);
            } else {
                el.textContent = current.toLocaleString();
            }
        }, 20);
    };

    // Trigger when section is in view
    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                counters.forEach(animateCounter);
                observer.disconnect();
            }
        });
    }, { threshold: 0.3 });

    const section = document.getElementById('pet-lover-info');
    if (section) observer.observe(section);
});

// Fun Facts Rotator
var funFacts = [
    "Dogs have a sense of smell that is 10,000 to 100,000 times more acute than humans!",
    "Cats spend 70% of their lives sleeping — that's about 13–16 hours a day! 😴",
    "A dog's nose print is as unique as a human fingerprint.",
    "Cats have 32 muscles in each ear, helping them rotate ears 180 degrees.",
    "Dogs can detect certain diseases in humans, including some cancers, through smell.",
    "A group of cats is called a 'clowder', and a group of kittens is called a 'kindle'.",
    "Dogs have three eyelids — including one for keeping the eye moist and protected.",
    "Cats can make over 100 different vocal sounds, while dogs can only make about 10."
];
var currentFact = 0;

function nextFact() {
    currentFact = (currentFact + 1) % funFacts.length;
    var box = document.querySelector('.fun-fact-text');
    if (box) {
        box.style.opacity = '0';
        setTimeout(function() {
            box.textContent = '"' + funFacts[currentFact] + '"';
            box.style.opacity = '1';
        }, 200);
        box.style.transition = 'opacity 0.2s';
    }
}
</script>